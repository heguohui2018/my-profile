import UIKit

//let关键字声明一个常量
let name = "hello world"
print(name)

let age = 20
print(age)

//var 声明一个变量
var number = 20
print(number)

var videoname = "one day"
print(videoname)

//更改变量的值
videoname = "two day"

//类型注释
let city:String = "china"
print(city)

let maxage:Int = 30
print(maxage)

let minage:Float = 30
print(minage)


